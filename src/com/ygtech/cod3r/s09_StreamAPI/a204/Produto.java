package com.ygtech.cod3r.s09_StreamAPI.a204;

class Produto {

	final String nome;
	final double preco; 
	final int desconto;
	final double frete;
	
	public Produto(String nome, double preco, int desconto, double frete) {
		super();
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.frete = frete;
	}
}
